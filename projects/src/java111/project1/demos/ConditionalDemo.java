/** A class to demonstrate conditional logic (if/else). 
 *  @author pwaite    
 */
public class ConditionalDemo {
    /** The main method to run our program 
     *  The method will create a variable and perform 
     *  conditional logic based on the value
     *  @param args command line arguments
     */
    public static void main(String[] args){ 
        int myCount = 100;
        if (myCount == 10) {
            System.out.println("The value is indeed 10");
        } else {
            System.out.println("The value is NOT 10");
        }
    }
}

