/** A class to demonstrate assigning values to variables. 
 *  @author pwaite    
 */
public class AssignmentDemo {
    /** The main method to run our program 
     *  The method will create variables of 3 types,
     *  assign values to the variables, and display them
     *  @param args command line arguments
     */
    public static void main(String[] args){ 
        int myInt = 300;
        double myDouble = 3.33;
        String myString = "Hello Everyone, I hope you are enjoying assignment";
        System.out.println("myInt is " + myInt);
        System.out.println("myDouble is " + myDouble);
        System.out.println("myString is " + myString);
    }
}

